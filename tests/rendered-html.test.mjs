import assert from "node:assert/strict";
import test from "node:test";

async function render() {
  const workerUrl = new URL("../dist/server/index.js", import.meta.url);
  workerUrl.searchParams.set("test", `${process.pid}-${Date.now()}`);
  const { default: worker } = await import(workerUrl.href);
  return worker.fetch(
    new Request("http://localhost/", { headers: { accept: "text/html" } }),
    { ASSETS: { fetch: async () => new Response("Not found", { status: 404 }) } },
    { waitUntil() {}, passThroughOnException() {} },
  );
}

test("server-renders the Expert XP agenda", async () => {
  const response = await render();
  assert.equal(response.status, 200);
  assert.match(response.headers.get("content-type") ?? "", /^text\/html\b/i);
  const html = await response.text();
  assert.match(html, /<title>Agenda Expert XP 2026<\/title>/i);
  assert.match(html, /Encontre sua próxima sessão/);
  assert.match(html, /Buscar palestra, tema ou palestrante/);
  assert.match(html, /Reinventar-se para Vencer/);
  assert.doesNotMatch(html, /codex-preview|Your site is taking shape/);
});
